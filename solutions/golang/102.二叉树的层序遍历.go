/**
 * Definition for a binary tree node.
 * type TreeNode struct {
 *     Val int
 *     Left *TreeNode
 *     Right *TreeNode
 * }
 */
package main

func levelOrder(root *TreeNode) [][]int {
	ans := [][]int{}
	if root == nil {
		return ans
	}
	queue := []*TreeNode{root}
	for len(queue) > 0 {
		lastIndex := len(queue) - 1
		cur := []int{}
		next := []*TreeNode{}
		for i := 0; lastIndex >= i; i++ {
			item := queue[i]
			cur = append(cur, item.Val)
			if item.Left != nil {
				next = append(next, item.Left)
			}
			if item.Right != nil {
				next = append(next, item.Right)
			}
		}
		ans = append(ans, cur)
		queue = next
	}
	return ans
}

